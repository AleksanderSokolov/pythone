# Выполнить логические побитовые операции "И", "ИЛИ" и др. над числами 5 и 6. Выполнить
# над числом 5 побитовый сдвиг вправо и влево на два знака.

print(5 | 6)  # или
print(5 & 6)  # и
print(5 ^ 6)  # исключительное или

print(~ 5)  # не
print(~ 6)  # не

print(5 >> 2)  # сдвиг вправо на 2 знака
print(5 << 2)  # сдвиг влева на 2 знака


